package beehive.bean;

public class Device {
	// Member variable
	public int deviceid;
	public String phone;
	public boolean devicestatus;
	public boolean autocontrol;
	public boolean hasco;
	public boolean haspm25;
	public boolean hasuv;
	public boolean hastemperature;
	public boolean hasnoise;
	public int getDeviceid() {
		return deviceid;
	}
	public void setDeviceid(int deviceid) {
		this.deviceid = deviceid;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public boolean isDevicestatus() {
		return devicestatus;
	}
	public void setDevicestatus(boolean devicestatus) {
		this.devicestatus = devicestatus;
	}
	public boolean isAutocontrol() {
		return autocontrol;
	}
	public void setAutocontrol(boolean autocontrol) {
		this.autocontrol = autocontrol;
	}
	public boolean isHasco() {
		return hasco;
	}
	public void setHasco(boolean hasco) {
		this.hasco = hasco;
	}
	public boolean isHaspm25() {
		return haspm25;
	}
	public void setHaspm25(boolean haspm25) {
		this.haspm25 = haspm25;
	}
	public boolean isHasuv() {
		return hasuv;
	}
	public void setHasuv(boolean hasuv) {
		this.hasuv = hasuv;
	}
	public boolean isHastemperature() {
		return hastemperature;
	}
	public void setHastemperature(boolean hastemperature) {
		this.hastemperature = hastemperature;
	}
	public boolean isHasnoise() {
		return hasnoise;
	}
	public void setHasnoise(boolean hasnoise) {
		this.hasnoise = hasnoise;
	}

	
	

}
